# Operadores matematicos em Python
# +   Adição
# -   Subtração
# *   Multiplicação
# /   Divisão 
# //  Divisão Inteira
# %   Módulo 'Resto da Divisão'
# **  Exponenciação

num1 = float(input('Digite um numero: '))
num2 = float(input('Digite outro numero: '))

print('\n ===== Operadores Matemáticos em Python ===== \n'.upper())
print(f'Adição:          {num1} + {num2} = {num1 + num2}')
print(f'Subtração:       {num1} - {num2} = {num1 - num2}')
print(f'Multiplicação:   {num1} * {num2} = {num1 * num2}')
print(f'Divisão:         {num1} / {num2} = {num1 / num2}')
print(f'Divisão Inteira: {num1} // {num2} = {num1 // num2}')
print(f'Módulo:          {num1} % {num2} = {num1 % num2}')
print(f'Exponenciação:   {num1} ** {num2} = {num1 ** num2}')
